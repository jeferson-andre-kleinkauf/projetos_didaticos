package hamburgueria;

public class Ingrediente {
	
	private String nome;
	private Hamburguer hambu;
	public Ingrediente() {
		
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public Hamburguer getHambu() {
		return hambu;
	}

	public void setHambu(Hamburguer hambu) {
		this.hambu = hambu;
	}
	
}
