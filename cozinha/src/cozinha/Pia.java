package cozinha;

public class Pia {
	public int gavetas;
	public int portas;
	public int cubas;
	public String cor;
	
	public Pia() {
		
	}

	public int getGavetas() {
		return gavetas;
	}

	public void setGavetas(int gavetas) {
		this.gavetas = gavetas;
	}

	public int getPortas() {
		return portas;
	}

	public void setPortas(int portas) {
		this.portas = portas;
	}

	public int getCubas() {
		return cubas;
	}

	public void setCubas(int cubas) {
		this.cubas = cubas;
	}

	public String getCor() {
		return cor;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}

}

