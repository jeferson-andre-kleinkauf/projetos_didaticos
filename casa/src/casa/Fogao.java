package casa;

public class Fogao {
	private int bocas;
	private String cor;
	private double tamanho;
	
	public Fogao() {
		
	}

	public int getBocas() {
		return bocas;
	}

	public void setBocas(int bocas) {
		this.bocas = bocas;
	}

	public String getCor() {
		return cor;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}

	public double getTamanho() {
		return tamanho;
	}

	public void setTamanho(double tamanho) {
		this.tamanho = tamanho;
	}
	
}
